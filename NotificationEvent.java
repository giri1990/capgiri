package com.ledify.notification.event.notification_event_publisher;

public class NotificationEvent {
	
	private String transType;
	private String sagreementNo;
	private String eventType;
	private String eventDtl;
	private String eventDt;
	public String getTransType() {
		return transType;
	}
	public void setTransType(String transType) {
		this.transType = transType;
	}
	public String getSagreementNo() {
		return sagreementNo;
	}
	public void setSagreementNo(String sagreementNo) {
		this.sagreementNo = sagreementNo;
	}
	public String getEventType() {
		return eventType;
	}
	public void setEventType(String eventType) {
		this.eventType = eventType;
	}
	public String getEventDtl() {
		return eventDtl;
	}
	public void setEventDtl(String eventDtl) {
		this.eventDtl = eventDtl;
	}
	public String getEventDt() {
		return eventDt;
	}
	public void setEventDt(String eventDt) {
		this.eventDt = eventDt;
	}
	
		

}
